package oops;

public class Employee {
	
	// Data members or fields 
	
	public int emp_id;
	public String emp_name;
	public double emp_sal;
	
	// Default Constructor
	public Employee()
	{
		
	}
	
	// Argument Constructor
	
	public Employee(int emp_id1,String emp_name1,double emp_sal1)
	{
		emp_id=emp_id1;
		emp_name=emp_name1;
		emp_sal=emp_sal1;
		
	}

}
