package coreJava.variables;

public class ReferenceTypeVariable {

	public static void main(String[] args) {
		
		String str1="welcome";
		
		
		System.out.println(str1);
		System.out.println(str1.endsWith("e"));
		System.out.println(str1.toUpperCase());
		System.out.println(str1.length());
		System.out.println(str1.concat(" Alex"));
		System.out.println(str1.charAt(2));
		System.out.println(str1.contains("wel"));
		
		String str2="java world";
		
		
		System.out.println(str2);
		System.out.println(str2.endsWith("e"));
		System.out.println(str2.toUpperCase());
		System.out.println(str2.length());
		System.out.println(str2.concat(" Alex"));
		System.out.println(str2.charAt(2));
		System.out.println(str2.contains("wel"));
		

	}

}
