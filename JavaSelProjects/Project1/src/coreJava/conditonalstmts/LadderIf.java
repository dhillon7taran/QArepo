package coreJava.conditonalstmts;

public class LadderIf {

	public static void main(String[] args) {

		char ch='o';
		
		if (ch=='a')
		{
			System.out.println(ch+" is a vowel");
		}
		else if(ch=='e')
		{
			System.out.println(ch+" is a vowel");
		}
		else if(ch=='i')
		{
			System.out.println(ch+" is a vowel");
		}
		else if(ch=='o')
		{
			System.out.println(ch+" is a vowel");
		}
		else if(ch=='u')
		{
			System.out.println(ch+" is a vowel");
		}
		else
		{
			System.out.println(ch+" is a consonant");
			
		}

	}

}
